
 class Shop 
{
	
	private	String name;
	private	int price;
		
	
	public String setItemName(String name) {
		this.name = name;
		return name;
	}
	
	public String getItemName() {
		return name;
	}
	
	public int setPrice(int price) {
		this.price = price;
		return price;
	}
	
	public int getPrice() {
		return price;
	}
	
	
}


